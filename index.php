                    <h2 class="projects-h3 margbot-small p-cen">Senior Capstone Project</h2>

                    <p class="popup__text popup__caps">
                    <b>Introduction</b><br>
                    My group capstone project is focused on setting up a secure connection from two external servers to our internal server. Two external servers are defined: Tailgate (TruckBed) server is a Ubuntu operating system that has all of its computer components connected in a small Georgia Southern Truck Bed. Cooler server is a Raspberry Pi operating system that are connected and powered with external batteries in a Georgia Southern Cooler. Our internal server is a private virtual machine (VMWare) that is connected to our private network. The purpose of our project is sync a deliverable file from one external server to our internal server and sync website files from our internal server to both external servers. Also, we set up Cron Jobs for automating rsync commands and extract, transform, load file into our internal database.
                    <br><br><br>

                    <b>Implementation Details</b><br>
                    After we closed our capstone project, we successfully set up automated rsync commands, developed PHP script for file extraction, transformation (into SQL queries), and upload into the database, and updated websites and system files. When a user fills out the form in our recruitment website, the form data will append to our deliverable file in one of the external server directory. When the external servers are returned to Georgia Southern and turned on, the automated rysnc command will execute by sending the deliverable file to the internal server. It will also delete the file from the source location. Until the time calls and the internal server is running, the automated PHP script extracts the contents of the file. The contents will bind and transform into SQL queries and then execute the query in the database. All contents are uploaded to the database and the file is successfully deleted.


