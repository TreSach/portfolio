                    <p class="popup__text">
                   A middleware application that collects information from 15 entities and logged them into a .csv file. Sensordata files are logged every hour to ensure all information are updated on time. This application will extract the entire contents of the sensordata files ready to check. Then, the application will filter out some of the 15 entities only if the condition is met. Of all 15 entities, if the battery charge is less than or equal to 50.0%, the application will filter them out and save them to the same sensordata file. However, all unfiltered data are deleted once the application completes the scan. Finally, the application will load the updated sensordata files into a folder for viewing. An example scenario will be running this application to get the sensordata files. Then, filter them out only if the conditions are met. After the application process the sensordata files, it will send them to a folder or email to the IT Manager. The IT Manager will address the affected entities that its battery needs to be replaced as soon as possible.

